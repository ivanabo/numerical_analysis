function [y,t] = AB(F, y0, f, t0, n, h)

y = zeros(n,1);
y(1:4,1) = y0(1:4,1);
t = zeros(n,1);
t(1:4)=t0(1:4);

for i = 4:n-1
    t(i+1) = t(i) + h;
    y(i+1) = y(i) + h*( 55*f(4) - 59*f(3) +37*f(2) - 9*f(1) )/24;
    f(1) = f(2);
    f(2) = f(3);
    f(3) = f(4);
    f(4) = F(t(i+1),y(i+1));
end
y(n) = y(n-1) + h*( 55*f(4) - 59*f(3) +37*f(2) - 9*f(1) )/24;
end

